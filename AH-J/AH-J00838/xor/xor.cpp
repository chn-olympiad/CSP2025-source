long long add(long long a,long long b)
{
    long long t=1;
    long long s=0;
    b=two[b];
    while(a!=0||b!=0)
    {
        if(a%10!=b%10)
        {
            s+=t*1;
        }
        else
        {
            s+=t*0;
        }
        t*=10;
        a/=10;
        b/=10;
    }
    return s;
}
