#include<bits/stdc++.h>
using namespace std;
long long n,a[5005],b[5005],ans,t;
int main()
{
    freopen("polygon.in","r",stdin);
    freopen("polygon.out","w",stdout);
    cin>>n;

    for(int i=1;i<=n;i++)
    {
        cin>>a[i];
        b[a[i]]++;
    }
    sort(a,a+n+1);
    if(n>=5)ans=n*(n-1)/2;

    for(int i=3;i<=n-3;i++)
    {
        ans+=ans*(n-i+1)/(n-i);
        ans%=998244353;
    }
    ans+=n+1;
    ans%=998244353;
    cout<<ans;
    return 0;
}
