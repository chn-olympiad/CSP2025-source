#include<iostream>
using namespace std;
int main()
{
	int n,p;
	string s,t;
	cin>>n,p;
	for(int i=1;i<=s.size();i++)
	{
		cin>>s[i];
	}
	for(int j=1;j<=t.size();j++)
	{
		cin>>t[j];
	}
	for(int i=1;i<=n;i++)
	{
		for(int j=1;j<=p;j++)
		{
			cout<<s[i]<<" "<<s[j]<<endl;
		}
	}
	return 0;
 } 
