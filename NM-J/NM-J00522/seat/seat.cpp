#include <cstdio>
#include <algorithm>
using namespace std;

int a[128], n, m, r;

bool cmp(int a, int b) {
	return a > b;
}

int main() {
	freopen("seat.in", "r", stdin);
	freopen("seat.out", "w", stdout);
	scanf("%d%d", &n, &m);
	for (int i = 1; i <= n * m; i++) {
		scanf("%d", &a[i]);
	}
	r = a[1];
	sort(a + 1, a + n*m + 1, cmp);
	int idx = 0;
	for (int j = 1; j <= m; j++) {
		if (j % 2 == 1) {
			for (int i = 1; i <= n; i++) {
				idx++;
				if (a[idx] == r) {
					printf("%d %d", i, j);
				}
			}
		} else {
			for (int i = n; i >= 1; i--) {
				idx++;
				if (a[idx] == r) {
					printf("%d %d", i, j);
				}
			}
		}
	}
	fclose(stdin);
	fclose(stdout);
	return 0;
}

