#include<bits/stdc++.h>
#include<cstdio>
using namespace std;
int n,m;
int main(){
	int a[n][m];
	freopen("seat.in","n",stdin);
	freopen("seat.in","m",stdin);
	for(int i=0;i<=n;i++){
		for(int j=0;j<=m;j++){
			freopen("seat.in","a[i][j]",stdin);
		}
	
	} 
	if(n==1&&m==1) freopen("seat.out","a[0][0]",stdout);
	return 0;
}
