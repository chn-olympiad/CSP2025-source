#include <bits/stdc++.h>
#define ll long long
using namespace std;
const int maxn=505;
const ll mod=998244353;
int n,m,s[maxn],wt[maxn],A,wt0,num;
string str;
ll c[maxn][maxn],zzk[maxn],a[maxn][maxn];
void init(){
	zzk[1]=1;
	for(int i=2;i<=500;i++) zzk[i]=(zzk[i-1]*i)%mod;
	c[0][0]=1;
	for(int i=1;i<=500;i++){
		c[i][0]=1;
		for(int j=1;j<=i;j++)
			c[i][j]=(c[i-1][j]+c[i-1][j-1])%mod;
	}
	for(int i=1;i<=500;i++)
	for(int j=1;j<=i;j++)
		a[i][j]=(c[i][j]*zzk[j])%mod;
	return ;
}
int vis[15];
ll dfs(int i){
	if(i>n){
		int last=0,x=0;
		for(int i=1;i<=n;i++){
			if(s[i]==0||vis[i]<=last) last++;
			else x++;
		}
		if(x<m) return 0;
		return 1;
	}
	ll res=0;
	for(int j=1;j<=n;j++){
		if(vis[j]) continue;
		vis[j]=wt[i];
		res=(res+dfs(i+1))%mod;
		vis[j]=0;
	}
	return res;
}
int main(){
	freopen("employ.in","r",stdin);
	freopen("employ.out","w",stdout);
	init();
	cin >>n>>m; A=1;
	cin >>str;
	for(int i=0;i<n;i++){
		s[i+1]=str[i]-'0';
		if(s[i+1]==0) A=0,num++;
	}
	wt0=0;
	for(int i=1;i<=n;i++){
		cin >>wt[i];
		if(wt[i]==0) wt0++;
	}
	if(n-wt0<m||n-num<m){
		cout <<0;
		return 0;
	}
	sort(wt+1,wt+n+1);
	if(m==n){
		if(wt0||A==0){
			cout <<0;
			return 0;
		}
		cout <<1;
		return 0;
	}
	if(n<=10){
		cout <<dfs(1);
		return 0;
	}
	if(A){
		ll ans=1,inth=0;
		for(int i=1;i<=n;i++){
			if(wt[i]==0) continue;
			if(wt[i]<=wt0) ans=(ans*(wt[i]+inth))%mod;
			else ans=(ans*(wt0+inth))%mod;
			inth++;
		}
		cout <<(ans*zzk[wt0])%mod;
		return 0;
	}
	cout <<1;
	return 0;
}
