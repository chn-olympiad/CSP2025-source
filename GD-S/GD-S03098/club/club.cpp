#include<bits/stdc++.h>
#define int long long
using namespace std;
int t,n,a[100005][5],cnt[5],b[100005],ans,len,to[100005];
signed main(){
	freopen("club.in","r",stdin);
	freopen("club.out","w",stdout);
	scanf("%lld",&t);
	while(t--){
		scanf("%lld",&n);ans=0;len=0;
		cnt[1]=cnt[2]=cnt[3]=0;
		for(int i=1;i<=n;++i){
			scanf("%lld%lld%lld",&a[i][1],&a[i][2],&a[i][3]);
			int tmp=max({a[i][1],a[i][2],a[i][3]});
			if(a[i][1]==tmp&&a[i][2]==tmp&&a[i][3]==tmp){
				int op=1;
				if(cnt[2]<cnt[op])op=2;
				if(cnt[3]<cnt[op])op=3;
				cnt[op]++;to[i]=op;
//				cout<<i<<" "<<op<<'\n';
				continue;
			}
			if(a[i][1]==tmp&&a[i][2]==tmp){
				int op=1;
				if(cnt[2]<cnt[op])op=2;
				cnt[op]++;to[i]=op;
//				cout<<i<<" "<<op<<'\n';
				continue;
			}
			if(a[i][1]==tmp&&a[i][3]==tmp){
				int op=1;
				if(cnt[3]<cnt[op])op=3;
				cnt[op]++;to[i]=op;
//				cout<<i<<" "<<op<<'\n';
				continue;
			} 
			if(a[i][2]==tmp&&a[i][3]==tmp){
				int op=2;
				if(cnt[3]<cnt[op])op=3;
				cnt[op]++;to[i]=op;
//				cout<<i<<" "<<op<<'\n';
				continue;
			}
			if(a[i][1]==tmp)cnt[1]++,to[i]=1;
			if(a[i][2]==tmp)cnt[2]++,to[i]=2;
			if(a[i][3]==tmp)cnt[3]++,to[i]=3;
//			cout<<i<<" "<<to[i]<<'\n'; 
		}
		for(int i=1;i<=n;++i)ans+=a[i][to[i]];
//		cout<<cnt[1]<<' '<<cnt[2]<<' '<<cnt[3]<<'\n';
		int op=0;
		if(cnt[1]>n/2)op=1;
		if(cnt[2]>n/2)op=2;
		if(cnt[3]>n/2)op=3;
//		cout<<op<<'\n';
//		cout<<ans<<'\n';
		if(op!=0){
			for(int i=1;i<=n;++i){
				int tmp=max({a[i][1],a[i][2],a[i][3]});
				if(a[i][op]==tmp&&to[i]==op){
					++len;
					if(op!=1)b[len]=a[i][1]-a[i][op];
					else b[len]=a[i][2]-a[i][1];
					for(int j=1;j<=3;++j)
						if(j!=op)
							b[len]=max(b[len],a[i][j]-a[i][op]);
				}
			}
			sort(b+1,b+len+1,greater<int>());
//			for(int i=1;i<=len;++i)
//				cout<<b[i]<<"\n";
			for(int i=1;i<=cnt[op]-n/2;++i)
				ans+=b[i];
			cout<<ans<<'\n';
		}else cout<<ans<<'\n';
	} 
	return 0;
} 
