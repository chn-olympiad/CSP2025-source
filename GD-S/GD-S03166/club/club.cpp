using namespace std;
const int N=1e5+5,sN=205;
int dp[sN][sN/2][sN/2]; 
int a[N][4];
struct stu{
	int a,b,c;
	bool operator()(stu x,stu y){
		if(x.a-x.b>y.a-y.b){
			return true;
		} else return false;
	}
};
bool bi(stu y,stu x){
	return y.a-x.a+x.b>=0;
}
