#include<bits/stdc++.h>
using namespace std;
struct stu{
	int a,b,c,final,id,num;
}v[100005];
bool cmp1(stu a,stu b){
	if(a.id!=b.id)return a.id<b.id;
	else if(a.id==1){
		if(min(a.a-a.b,a.a-a.c)<min(b.a-b.b,b.a-b.c))return min(a.a-a.b,a.a-a.c);
		else return a.num<b.num;  
	}
	else return a.num<b.num;
}
bool cmp2(stu a,stu b){
	if(a.id!=b.id)return a.id<b.id;
	else if(a.id==2){
		if(min(a.b-a.a,a.b-a.c)<min(b.b-b.a,b.b-b.c))return min(a.b-a.a,a.b-a.c);
		else return a.num<b.num;  
	}
	else return a.num<b.num;
}
bool cmp3(stu a,stu b){
	if(a.id!=b.id)return a.id<b.id;
	else if(a.id==3){
		if(min(a.c-a.b,a.c-a.a)<min(b.c-b.b,b.c-b.a))return min(a.c-a.b,a.c-a.a);
		else return a.num<b.num;  
	}
	else return a.num<b.num;
}
int main(){
	freopen("club.in","r",stdin);
	freopen("club.out","w",stdout);
	int T;
	scanf("%d",&T);
	while(T--){
		int n,sa=0,sb=0,sc=0;
		long long sum=0;
		scanf("%d",&n);
		for(int i=1;i<=n;i++){
			v[i].num=i;
			scanf("%d%d%d",&v[i].a,&v[i].b,&v[i].c);
			v[i].final=max(v[i].a,max(v[i].b,v[i].c));
			if(v[i].final==v[i].a)v[i].id=1,sa++;
			if(v[i].final==v[i].b)v[i].id=2,sb++;
			if(v[i].final==v[i].c)v[i].id=3,sc++;
			sum+=v[i].final;
		}
		int stand=n/2;
		if(sa>stand){
			int dis=sa-stand;
			sort(v+1,v+n+1,cmp1);
			for(int i=1;i<=n;i++){
				if(v[i].id==1){
					if(v[i].a-v[i].b<v[i].a-v[i].c){
						v[i].id=2;
						sum=sum-v[i].a+v[i].b;
						dis--; 
						sb++;
					}
					else{
						v[i].id=3;
						sum=sum-v[i].a+v[i].c;
						dis--; 
						sc++;
					}
				}
				if(dis==0)break;
			}
		}
		if(sb>stand){
			int dis=sb-stand;
			sort(v+1,v+n+1,cmp2);
			for(int i=1;i<=n;i++){
				if(v[i].id==2){
					if(v[i].b-v[i].a<v[i].b-v[i].c){
						v[i].id=1;
						sum=sum-v[i].b+v[i].a;
						dis--; 
						sa++;
					}
					else{
						v[i].id=3;
						sum=sum-v[i].b+v[i].c;
						dis--; 
						sc++;
					}
				}
				if(dis==0)break;
			}
		}
		if(sc>stand){
			int dis=sc-stand;
			sort(v+1,v+n+1,cmp3);
			for(int i=1;i<=n;i++){
				
				if(v[i].id==3){
					if(v[i].c-v[i].b<v[i].c-v[i].a){
						v[i].id=2;
						sum=sum-v[i].c+v[i].b;
						dis--; 
						sb++;
					}
					else{
						v[i].id=1;
						sum=sum-v[i].c+v[i].a;
						dis--; 
						sa++;
					}
				}
				if(dis==0)break;
			}
		}
		cout<<sum<<"\n";
	}
	return 0;
}
