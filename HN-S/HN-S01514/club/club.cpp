#include<bits/stdc++.h>
using namespace std;
int t,n;
int a[100005][5]; 
int main(){
	freopen("club.in","r",stdin);
	freopen("club.out","w",stdout);
	scanf("%d",&t);
	while(t--){
		scanf("%d",&n);
		for(int i=1;i<=n;i++){
			scanf("%d",&a[n][i]);
		}
		
	}
	fclose(stdin);
	fclose(stdout);
	return 0;
}
