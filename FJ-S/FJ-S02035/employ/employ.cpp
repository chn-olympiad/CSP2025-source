long long dfs(int t,int su){
	if(n-t+1<m-su)return 0;
	if(t>n)return 0;
	if(su>=m)return jc[max(0,n-t+1)];
	long long res=0;
	for(int i=1;i<=n;i++){
		if(vis[i])continue;
		vis[i]=1;f[t]=i;
		res+=dfs(t+1,su+(s[i]-'0'));
		vis[i]=0;f[t]=0;}
	return res;}
