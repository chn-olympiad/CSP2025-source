#include<bits/stdc++.h>
using namespace std;
const int mod = 998244353;
const int maxn = 5e5 + 5;
int n;
int a[maxn];
int ans;
inline int fastpow(int power, int base)
{
	int res = 1;
	while(power > 0)
	{
		if(power & 1)
		{
			res *= base;
			res = res % mod;
		}
		power >>= 1;
		base *= base;
	}
	return res % mod;
}
