int read() {
	int x=0,f=1;
	char ch=getchar();
	while(ch=='-') {
		f=-1;
	}
	while(ch<='9'&&ch>='0') {
		x=x*10+(ch-'0');
		ch=getchar();
	}
	return x*f;
}
