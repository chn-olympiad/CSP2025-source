#include<bits/stdc++.h>
using namespace std;

int n, m;
const int maxn = 505;
const int maxm = 505;
char s[maxn];
int c[maxn];

#define lowbit(x) (x & (-x))
int dp[1 << 20][20]; // 'j' people have been out of the work
int uid[1 << 20];
int num1[1 << 20];

const int mod = 998244353;
void Mod(int &x) {if(x >= mod) x -= mod; else if(x < 0) x += mod;}

int cc() {
    for(int i = 1, t = 0; t < n; t ++, i <<= 1) {
        uid[i] = t;
        if(s[1] == '0') dp[i][1] = 1;
        else {
            if(c[t]) dp[i][0] = 1;
            else dp[i][1] = 1;
        }
    }
    int full = (1 << n) - 1;
    for(int i = 1; i <= full; i ++) num1[i] = num1[i ^ lowbit(i)] + 1;

    for(int cur = 3; cur <= full; cur ++) {
        if(!(num1[cur] ^ 1)) continue;
        for(int las, j = lowbit(cur), S = cur; S; S ^= j, j = lowbit(S)) {
            las = cur ^ j;
            for(int v = 0; v <= num1[las]; v ++) {
                if(v >= c[uid[j]] || s[num1[cur]] == '0') {
                    dp[cur][v + 1] += dp[las][v];
                    Mod(dp[cur][v + 1]);
                }
                else {
                    dp[cur][v] += dp[las][v];
                    Mod(dp[cur][v]);
                }
            }
        }
    }
    int ans = 0;
    for(int i = 0; i <= n - m; i ++) {
        ans += dp[full][i];
        Mod(ans);
    }
    return ans;
}

int main() {
    freopen("employ.in", "r", stdin);
    freopen("employ.out", "w", stdout);
    scanf("%d%d", &n, &m);
    scanf("%s", s + 1);
    for(int i = 0; i < n; i ++) scanf("%d", &c[i]);
    if(n <= 19) printf("%d\n", cc());
    else printf("0\n");
    return 0;
}
