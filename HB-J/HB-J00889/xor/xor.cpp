#include<bits/stdc++.h>
using namespace std;
using ll = long long;
ll a[100001];
inline ll f(int l,int r){
    return a[r]^a[l-1];
}
int main(){
    freopen("xor.in","r",stdin);
    freopen("xor.out","w",stdout);
    a[0] = 0;
    int n,l = 1,k,r,ans = 0;
    cin >> n >> k;
    for(int i = 1;i <= n;++i){
        ll x;cin >> x;
        a[i] = a[i-1]^x;
    }
    for(r = 1;r <= n;++r){
        if(f(l,r) == k){
            l = r+1;
            ++ans;
        }
    }
    cout << ans;
    return 0;
}
