#include <bits/stdc++.h>
using namespace std;
int n,m,k;
vector<pair<int, int>> edges[10086];
bool went[10086];
int main()
{
	freopen("employ.in", "r", stdin);
	freopen("employ.out", "w", stdout);
	cin >> n >> m >> k;
	for(int i = 1,u,v,w; i <= m; i++)
	  {
	    cin >> u >> v >> w;
	    edges[u].push_back({v,w});
	    edges[v].push_back({u,w});
	  }
	for(int i = m+1,u,v,w; i <= m+k; i++)
	{
	    cin >> u >> v >> w;
	    edges[u].push_back({v,w});
	    edges[v].push_back({u,w});
	}
	
	fclose(stdin);
	fclose(stdout);
}
