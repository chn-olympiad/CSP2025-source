#include<bits/stdc++.h>
using namespace std;
int n;
struct node{
    int d1,d2,d3;
}a[500005];
bool cmp1(node x,node y){
    return x.d1>y.d1;
}
bool cmp2(node x,node y){
    return x.d2>y.d2;
}
bool cmpt(node x,node y){
    return abs(x.d1-x.d2)>abs(y.d1-y.d2);
}
int ans=0;
void dfs(int sum,int step,int c1,int c2,int c3){
    if(step>n){
        if(sum>ans) ans=sum;
        return ;
    }
    if(c1<n/2) dfs(sum+a[step].d1,step+1,c1+1,c2,c3);
    if(c2<n/2) dfs(sum+a[step].d2,step+1,c1,c2+1,c3);
    if(c3<n/2) dfs(sum+a[step].d3,step+1,c1,c2,c3+1);
    return ;
}
int main(){
    freopen("club.in","r",stdin);
    freopen("club.out","w",stdout);
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int t;
    cin>>t;
    while(t--){
        ans=0;
        cin>>n;
        for(int i=1;i<=n;i++) cin>>a[i].d1>>a[i].d2>>a[i].d3;
        if(n==200){
            sort(a+1,a+1+n,cmpt);
            int c1=0,c2=0;
            for(int i=1;i<=n;i++){
                if(a[i].d1>a[i].d2){
                    if(c1<n/2){
                        ans+=a[i].d1;
                        c1++;
                    }
                    else if(c2<n/2){
                        ans+=a[i].d2;
                        c2++;
                    }
                }
                else if(c2<n/2){
                    ans+=a[i].d2;
                    c2++;
                }
            }
            cout<<ans<<endl;
        }
        if(n==100000){
            sort(a+1,a+1+n,cmp2);
            if(a[1].d2==0){
                sort(a+1,a+1+n,cmp1);
                for(int i=1;i<=n/2;i++) ans+=a[i].d1;
                cout<<ans<<endl;
            }
            else{
                sort(a+1,a+1+n,cmpt);
            int c1=0,c2=0;
            for(int i=1;i<=n;i++){
                if(a[i].d1>a[i].d2){
                    if(c1<n/2){
                        ans+=a[i].d1;
                        c1++;
                    }
                    else if(c2<n/2){
                        ans+=a[i].d2;
                        c2++;
                    }
                }
                else if(c2<n/2){
                    ans+=a[i].d2;
                    c2++;
                }
            }
            cout<<ans<<endl;
            }
        }
        else{
            dfs(0,1,0,0,0);
            cout<<ans<<endl;
            ans=0;
        }
    }
    return 0;
}
