using namespace std;

int a[5005] , t = 1;
long long ans = 0;
int n;

void huisu(int maxx , int num , int x , int q){
	if(x >= 3){
		if(num > maxx * 2){
			ans = (ans + 1) % 988244353;
		}
	}
	
	for(int i = q;i <= n;i++){
		huisu(max(maxx , a[i]) , num+a[i] , x+1 , i+1);
	}
}

int main(){
	freopen("polygon.in" , "r" , stdin);
	freopen("polygon.out" , "w" , stdout);
	
	cin >> n;
	
	for(int i = 1;i <= n;i++){
		cin >> a[i];
		if(a[i] != 1){
			t = 0;
		}
	} 
	if(t == 0){
		huisu(0 , 0 , 0 , 1);
		cout<<ans;
		return 0;
	}
	
	for(int i = 4;i <= n;i++){
		long long num = 1;
		for(int j = 0;j < i;j++){
			num = num * (n - j) / (j+1) % 988244353;
		}
		ans = (ans + num) % 988244353;
	}
	cout<<ans;
	
	return 0;
}

