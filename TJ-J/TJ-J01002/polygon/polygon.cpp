#include <bits/stdc++.h>

using namespace std;

int n, k, a[500010];

int main() {
	freopen("xor.in", "r", stdin);
	freopen("xor.out", "w", stdout);
	cin >> n >> k;
	for (int i = 0; i < n; i++) {
		cin >> a[i];
	}
	if (n == 1 && a[0] == k) {
		cout << 1;
	} else if (n == 2 && (a[0]^a[1]) == k) {
		cout << 1;
	}
	cout << 2; 
	return 0;
} #include <bits/stdc++.h>

