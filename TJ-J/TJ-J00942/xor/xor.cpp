#include<bits/stdc++.h>
using namespace std;
int main(){
	freopen("xor.in","r",stdin);
	freopen("xor.out","w",stdout);
	int n=0,k=0;
	cin>>n>>k;
	//cout<<k<<endl;
	int a[100010];
	int flag=0,flagt=0;
	for(int i=1;i<=n;i++){
		cin>>a[i];
		if(a[i]!=1){
			flag++;
			if(a[i]!=0) flagt++;
		} 
		 
	}
	//cout<<flag;
	if(!flag) cout<<n/2;
	if(!flagt){
		if(k==1) cout<<n-flag;
		else cout<<(n-flag)/2+flag;
	} 
	fclose(stdin);
	fclose(stdout);
	return 0;
}
