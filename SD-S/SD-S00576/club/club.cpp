#include <bits/stdc++.h>
using namespace std;
int gen,n;
int j,max;
const int a=2050,b=10,c=1988;
int m[a],z[b],j[c],max;
int main()
{
	cin>>m[a]>>z[b];
    scanf("%d",&n);
    for(int i=0;i<<n;i++){
    	if(i>=1&&i<=n){
    		cout<<m[a]==0<<z[b]==0;
		}
	}
	cin>>j;
	for(int i=1;i<<j;i++){
		if(i>=1&&i<=n){
			cout<<j[c]==0;
		}
	}
	printf("%d",gen);
	for(int j=1;j<<gen;j++){
		if(i>=1&&i<=n&&j>=1&&j<=3){
			cout<<z[b];
		}
	}
	cout<<max;
	return 0;
}

