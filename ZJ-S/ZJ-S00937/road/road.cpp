#include<bits/stdc++.h>
using namespace std;
int n,m,k,u,v,wt,f,ans;
int h[11000],e[2100000],w[2100000],nxt[2100000],vis[11000],a[11000];
typedef pair<int,int> pii;
pii temp;
priority_queue<pii,vector<pii>,greater<pii> >q;
int main(){
	freopen("road.in","r",stdin);
	freopen("road.out","w",stdout);
	ios::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
	cin>>n>>m>>k;
	memset(h,0,sizeof(h));
	memset(vis,0,sizeof(vis));
	ans=0;
	for(int i=1;i<=m;i++){
		cin>>u>>v>>wt;
		e[i]=v;
		w[i]=wt;
		nxt[i]=h[u];
		h[u]=i;
		e[i+m]=u;
		w[i+m]=wt;
		nxt[i+m]=h[v];
		h[v]=i+m;
	}
	for(int i=1;i<=k;i++){
		cin>>a[0];
		for(int j=1;j<=n;j++){
			cin>>a[j];
			if(!a[j])f=j;
		}
		for(int j=1;j<=n;j++){
			if(f==j)continue;
			e[2*m+(i-1)*n+j]=j;
			w[2*m+(i-1)*n+j]=a[j];
			nxt[2*m+(i-1)*n+j]=h[f];
			h[f]=2*m+(i-1)*n+j;
			e[2*m+(2*i-1)*n+j]=f;
			w[2*m+(2*i-1)*n+j]=a[j];
			nxt[2*m+(2*i-1)*n+j]=h[j];
			h[j]=2*m+(2*i-1)*n+j;
		}
	}
	vis[1]=1;
	for(int i=h[1];i;i=nxt[i]){
		q.push({w[i],e[i]});
	}
	while(!q.empty()){
		temp=q.top();
		q.pop();
		if(vis[temp.second]){
			continue;
		}
		vis[temp.second]=1;
		ans+=temp.first;
		for(int i=h[temp.second];i;i=nxt[i]){
			if(vis[e[i]])continue;
			q.push({w[i],e[i]});
		}
	}
	cout<<ans<<'\n';
	return 0;
}
