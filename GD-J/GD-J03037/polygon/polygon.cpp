void dfs(int i){
	if(i==n+1){
		if(sum>2*Max){
			ans++;
			ans=ans%mod;
		}
		return;
	}
	dfs(i+1);
	sum+=a[i];
	int temp=Max;
	Max=max(Max,a[i]);
	dfs(i+1);
	sum-=a[i];
	Max=temp;
	return;
}

