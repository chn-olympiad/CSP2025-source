#include<bits/stdc++.h>
using namespace std;
int a[200],ans[100][100];
bool I_love_lyl(int x,int y){return x>y;}
int main(){
	freopen("seat.in","r",stdin);
	freopen("seat.out","w",stdout);
	int n,m;cin>>n>>m;
	for(int i=1;i<=n*m;i++){
		cin>>a[i];
	}
	int lyl=a[1];
	sort(a+1,a+n*m+1,I_love_lyl);
	int cnt=1,x=1,y=1;
	for(int i=1;i<=n*m;i++){
		ans[x][y]=a[cnt++];
		if(y%2==1&&x==n)y++;
		else if(y%2==0&&x==1)y++;
		else if(y%2==1)x++;
		else x--; 
	}
	for(int i=1;i<=n;i++){
		for(int j=1;j<=m;j++){
			if(ans[i][j]==lyl){
				cout<<j<<" "<<i;
				return 0;
			}
		}
	}
	return 0;
} 

