#include<bits/stdc++.h>
#define moos 998244353
using namespace std;
int vis=0;
int n;
int a[5050];
long long ans;
int maxn=-1;
int main()
{
	freopen("polygon.in","r",stdin);
	freopen("polygon.out","w",stdout);
	cin>>n;
	for(int i=1;i<=n;i++)
	{
		scanf("%d",&a[i]);
		if(a[i]!=1)
		{
			vis=1;
		}
	}
	if(n==3)
	{
		int sum=0;
		for(int i=1;i<=n;i++)
		{
			sum+=a[i];
			maxn=max(maxn,a[i]);
		}
		if(sum>2*maxn)
		{
			cout<<1;
			return 0;
		}
		else
		{
			cout<<0;
			return 0;
		}
	}
	if(vis==0)
	{
		for(int i=3;i<=n;i++)
		{
			long long fenzi=1,fenmu=1;
			for(int j=n;j>=n-i+1;j--)
			{
				fenzi*=j;
				fenzi%=moos;
			}
			for(int j=i;j>=1;j--)
			{
				fenmu*=j;
				fenmu%=moos;
			}
			ans+=(fenzi/fenmu)%moos;
		}
		cout<<ans%moos;
	}
	return 0;
}
