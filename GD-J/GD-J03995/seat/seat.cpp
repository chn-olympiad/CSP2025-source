#include<bits/stdc++.h>
using namespace std;
int a[105];
int main(){
	freopen("seat.in","r",stdin);
	freopen("seat.out","w",stdout);
	int n,m;
	scanf("%d%d",&n,&m);
	for(int i=1;i<=n*m;i++){
		scanf("%d",&a[i]);
	}
	int s=a[1];
	sort(a+1,a+n*m+1,greater<int>());
//	for(int i=1;i<=n*m;i++)cout<<a[i]<<" ";
	int p=1;
	for(;p<=n*m;p++){
		if(a[p]==s)break;
	}
	if(p%n==0){
		cout<<p/n<<" ";
		if(p/n%2==0)cout<<1;
		else cout<<n; 
	}else{
		cout<<p/n+1<<" ";
		if(p/n%2==0){
			cout<<p%n;
		}else{
			cout<<n-p%n+1;
		}
	}
	return 0;
}
