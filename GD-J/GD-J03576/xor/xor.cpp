#include <bits/stdc++.h>
using namespace std;
int a[600001];
int s[600001];
bool flag;
int n, k, ans;
void work(int cnt) {
	if (flag) {
		return;
	}
	int sum = a[cnt];
	if (sum == k && !s[cnt]) {
		s[cnt] = true;
//		cout << cnt << endl;
		ans++;
		work(cnt + 1);
		return;
	}
	for (int i = cnt + 1; i <= n; i++) {
		sum ^= a[i];
		if (sum == k && !s[cnt]) {
			ans++;
			s[cnt] = true;
//			cout << cnt << endl;
			cnt = i;
			work(cnt + 1);
			if (i == n) {
				flag = true;
				return;
			}
			return;
		}
	}
	return;
}
int main() {
	freopen("xor.in","r",stdin);
	freopen("xor.out","w",stdout);
	cin >> n >> k;
	for (int i = 1; i <= n; i++) {
		cin >> a[i];
	}
	work(1);
	cout << ans;
	return 0;
}
