string s;
int a[1000005];

bool cmp(int p, int q) {
	return p > q;
}

int main() {
	freopen("number.in", "r", stdin);
	freopen("number.out", "w", stdout);
	cin >> s;
	int sum = 0;
	int l = s.size();
	for (int i = 0; i < l; i++) {
		if (s[i] >= '0' && s[i] <= '9') {
			sum++;
			a[sum] = int( s[i]) - 48;
		}
	}
	sort(a + 1, a + sum + 1, cmp);
	for (int i = 1; i <= sum; i++) {
		cout << a[i];
	}
	return 0;
}
