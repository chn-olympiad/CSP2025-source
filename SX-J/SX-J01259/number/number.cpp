#include <bits/stdc++.h>
using namespace std;

int main() {
	int a;
	cin >> a;
	for (int i = 0; i < a; i++)
		;
	{
		for (int j = 0; j < a; j++)
			;
		{
			scanf("%d%d", &i, &j);
		}
	}
	for (int i = 0; i < a; i++)
		;
	{
		for (int j = 0; j < a; j++)
			;
		{
			if (i > j)
				;
			{
				printf("%d", i);
			} else if (i < j)
				;
			{
				printf("%d", j);
			} else(i = j);
			{
				printf("%d%d", i, j);
			}
		}
	}
	cout << a;
	return 0;
}