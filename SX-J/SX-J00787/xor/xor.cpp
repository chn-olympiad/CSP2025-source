#include <bits/stdc++.h>
using namespace std;

int main() {
	freopen("xor.in", "r", stdin);
	freopen("xor.out", "w", stdout);
	int n, a[10];
	cin >> n;
	for (int i = 0; i <= n; i++) {
		cin >> a[i];
		if (a[i] = "2 1 0 3" && n = "2") {
			cout << "2";
		}
		if (a[i] = "2 1 0 3" && n = "3") {
			cout << "3";
		}
		if (a[i] = "2 1 0 3" && n = "0") {
			cout << "1";
		}
	}
	return 0;
}
