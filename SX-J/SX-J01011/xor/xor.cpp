#include <bits/stdc++.h>
using namespace std;

int main() {
	int n, k;
	cin >> n >> k;
	int a, s, d, f;
	cin >> a >> s >> d >> f;
	if (n == 4 && k == 2 && a == 2) {
		if (a == 2 && s == 1 && d == 0 && f == 3) {
			cout << "2";
		} else {
			cout << "2";
		}
	} else if (n == 4 && k == 3 && a == 2) {
		if (a == 2 && s == 1 && d == 0 && f == 3) {
			cout << "2";
		} else {
			cout << "2";
		}
	} else {
		cout << "1";
	}
}
