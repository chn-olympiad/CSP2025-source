#include <bits/stdc++.h>
using namespace std;

int main() {
	int a[100], c = 0, r = 0, m, k = 0;
	cin >> c, r;
	k = c * r;
	for (int i = 0; i < k; i++) {
		cin >> a[i];
		m = a[i];
		if (a[i] > a[i]) {
			m = a[i];
		} else {
			m = a[i];
		}
	}
	cout << m;
	freopen("seat.in", "r", stdin);
	freopen("seatout", "w", stdout);
}