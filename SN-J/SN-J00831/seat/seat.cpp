#include <bits/c++.h>
using namespace std;
int main()
{
	int n,m,num,a[100][100];
	cin>>n>>m;
	for(int i=n;i<=num;i++){
		for(int j=m;j<=num;j++){
		    if(a[1]>a[i][j]){
		    	cin>>a[1][j];
			}
			else{
				cin>>a[i][i+1];
			}
		}
	}  
	cout<<i<<" "<<j;                             
	return 0;
}
