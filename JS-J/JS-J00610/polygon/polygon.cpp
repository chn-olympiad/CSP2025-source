#include<bits/stdc++.h>
using namespace std;
//"RP++RP++RP++RP++RP++"
//"------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------"
int n,a[5005];
long long cnt=0;
int main(){
    freopen("polygon.in","r",stdin);
    freopen("polygon.out","w",stdout);
    cin.tie(0),cout.tie(0);
    cin >> n;
    for(int i=0;i<n;i++)cin >> a[i];
    sort(a,a+n);
    if(n<3){
        cout << 0;
        return 0;
    }
    if(n==3){
        if(a[0]+a[1]>a[2])cout << 1;
        else cout << 0;
        return 0;
    }
    if(n==4){
        if(a[0]+a[1]>a[2])cnt++;
        if(a[0]+a[1]>a[3])cnt++;
        if(a[0]+a[2]>a[3])cnt++;
        if(a[1]+a[2]>a[3])cnt++;
        if(a[0]+a[1]+a[2]>a[3])cnt++;
        cout << cnt;
        return 0;
    }else if(n==5){
        if(a[0]+a[1]>a[2])cnt++;
        if(a[0]+a[1]>a[3])cnt++;
        if(a[0]+a[2]>a[3])cnt++;
        if(a[1]+a[2]>a[3])cnt++;
        if(a[0]+a[1]+a[2]>a[3])cnt++;
        if(a[0]+a[2]>a[4])cnt++;
        if(a[0]+a[1]>a[4])cnt++;
        if(a[0]+a[3]>a[4])cnt++;
        if(a[1]+a[2]>a[4])cnt++;
        if(a[1]+a[3]>a[4])cnt++;
        if(a[2]+a[3]>a[4])cnt++;
        if(a[0]+a[1]+a[2]>a[4])cnt++;
        if(a[0]+a[1]+a[3]>a[4])cnt++;
        if(a[0]+a[2]+a[3]>a[4])cnt++;
        if(a[1]+a[2]+a[3]>a[4])cnt++;
        if(a[0]+a[1]+a[2]+a[3]>a[4])cnt++;
        cout << cnt;
        return 0;
    }else if(n==6){
        for(int i=0;i<n;i++){
            for(int j=i+1;j<n;j++){
                for(int k=j+1;k<n;k++){
                    if(a[i]+a[j]>a[k])cnt++;
                }
            }
        }
        for(int i=0;i<n;i++){
            for(int j=i+1;j<n;j++){
                for(int k=j+1;k<n;k++){
                    for(int l=0;l<n;l++){
                        if(a[i]+a[j]+a[k]>a[l])cnt++;
                    }
                }
            }
        }
        for(int i=0;i<n;i++){
            for(int j=i+1;j<n;j++){
                for(int k=j+1;k<n;k++){
                    for(int l=0;l<n;l++){
                        for(int o=0;o<n;o++){
                            if(a[i]+a[j]+a[k]+a[l]>a[o])cnt++;
                        }
                    }
                }
            }
        }
        if(a[0]+a[1]+a[2]+a[3]+a[4]>a[5])cnt++;
        cout << cnt;
        return 0;
    }
    return 0;
}
