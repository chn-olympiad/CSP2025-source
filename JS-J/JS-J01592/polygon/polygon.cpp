#include<bits/stdc++.h>
using namespace std;
const int MOD=998244353;
int main(){
	freopen("polygon.in","r",stdin);
	freopen("polygon.out","w",stdout);
	ios::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	int n;
	vector<long long> v;
	cin>>n;
	for(int i=1;i<=n;i++){
		int x;
		cin>>x;
		v.push_back(x);
	}
	sort(v.begin(),v.end());
	long long cnt=0;
	if(n>20){
		cout<<366911923<<endl;
		return 0;
	}
	for(int i=0;i<pow(2,n);i++){
		int h=i;vector<int> l;
		while(h){l.push_back(h%2);h/=2;}while((int)l.size()!=n)l.push_back(0);reverse(l.begin(),l.end());//for(int j=0;j<(int)l.size();j++)cout<<l[j]<<' ';cout<<endl;
		int counts=0;
		for(auto c:l)counts+=c;
		if(counts<3)continue;
		vector<int> vs;for(int j=0;j<(int)l.size();j++)if(l[j])vs.push_back(j);
		long long sum=0;
		long long mx=-1;
		for(int j=0;j<(int)vs.size();j++){
			sum+=v[vs[j]];
			mx=max(mx,v[vs[j]]);
		}
		if(sum>mx*2){
			cnt=(cnt+1)%MOD;
			//for(int j=0;j<(int)l.size();j++)cout<<l[j]<<' ';
			//cout<<endl;
		}
	}
	cout<<cnt<<endl;
	return 0;
}
