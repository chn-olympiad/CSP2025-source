#include<bits/stdc++.h>
using namespace std;
int n,a[505],vis[505],t[505],ans;
void dfs(int last,int m){
	if(last==n){
		if(m>3){
			int mx=INT_MIN,all=0;
			for(int i=1;i<=n;i++){
				if(vis[i]){
					all+=a[i];
					mx=max(mx,a[i]);
				}
			}
			if(all>mx*2) ans++;
		}
	}
	vis[m+1]=1;
	dfs(last+1,m+1);
	vis[m+1]=0;
	dfs(last+1,m);
}
int main(){
	freopen("polygon.in","r",stdin);
	freopen("polygon.out","w",stdout);
	cin>>n;
	for(int i=1;i<=n;i++){
		cin>>a[i];
	}
	dfs(0,0);
	cout<<ans;
}
