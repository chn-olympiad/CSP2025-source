#include<bits/stdc++.h>
using namespace std;
#define N 205
typedef long long ll;
ll st[N], f;
ll n, m, tot;
void solve()
{
	ll x = 1, y = 1, h = 0;
	while(h < tot)
	{
		while(x < n && h < tot)
		{
			++h;
			if(st[h] == f)
			{
				cout << y << ' ' << x;
				return;
			}
			++x;
		}
		++h;
		if(st[h] == f)
		{
			cout << y << ' ' << x;
			return;
		}
		++y;
		while(x > 1 && h < tot)
		{
			++h;
			if(st[h] == f)
			{
				cout << y << ' ' << x;
				return;
			}
			--x;
		}
		++h;
		if(st[h] == f)
		{
			cout << y << ' ' << x;
			return;
		}
		++y;
	}
}
bool cmp(ll a, ll b)
{
	return a > b;
} 
int main()
{
	freopen("seat.in", "r", stdin);
	freopen("seat.out", "w", stdout);
	cin >> n >> m;
	tot = n * m;
	for(ll i = 1; i <= tot; i++)
	{
		cin >> st[i];
	}
	f = st[1];
	sort(st + 1, st + tot + 1, cmp);
	solve();
	return 0;
}
