#include<bits/stdc++.h>
using namespace std;
int n, a[1005], ans = 0;
int main(){
	freopen("polygon.in", "r", stdin);
	freopen("polygon.out", "w", stdout);
	cin >> n;
	for(int i = 1; i <= n; i++)
		cin >> a[i];
	for(int i = 1; i <= a[i]; i++)
		if(ans = a[i])
			ans += n;
		ans++;
		cout << ans;
	return 0;
}
