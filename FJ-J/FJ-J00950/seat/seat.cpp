#include<bits/stdc++.h>
using namespace std;
long long a[1000];
bool cmp(long long a,long long b){
	return a>b;
}
long long peo;
long long n,m,e;
long long vis[1000][1000];
long long fx[]={0,-1,1,0};
long long fy[]={0,0,0,1};
void dfs(long long x,long long y,long long step){
	if(step>peo)return;
	if(a[step]==e){
		cout<<y<<" "<<x;
		exit(0);
	}
	for(long long i=1;i<=3;i++){
		long long xx=fx[i]+x;
		long long yy=fy[i]+y;
		if(xx>=1&&xx<=n&&yy>=1&&yy<=m&&vis[xx][yy]==0){
			vis[x][y]=1;
			dfs(xx,yy,step+1);
			continue;
		}
	}
}

int main(){
	freopen("seat.in","r",stdin);
	freopen("seat.out","w",stdout);
	cin>>n>>m;
	peo=n*m;
	for(long long i=1;i<=peo;i++){
		cin>>a[i];
		if(i==1){
			e=a[i];
		}
	}
	sort(a+1,a+1+peo,cmp);
	dfs(1,1,1);
	return 0;
} 
